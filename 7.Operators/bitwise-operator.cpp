#include<iostream>
using namespace std;

int main()
{
    //& operator
    int ans= 2&3;
    cout<<ans<<endl;

    //| operator
    int ant= 2|3;
    cout<<ant<<endl;

    //l~ operator
    int anu= ~3;
    cout<<anu<<endl;

    //<< operator
    int anv= 10<<3;
    cout<<anv<<endl;

    //>> operator
    int anw= 10>>3;
    cout<<anw<<endl;
}